
#!/bin/sh

echo "================================================================================================================================"
echo "Prepairing python ..."

sudo apt update
sudo sudo apt install build-essential zlib1g-dev libncurses5-dev libgdbm-dev libnss3-dev libssl-dev libreadline-dev libffi-dev curl libbz2-dev


WORKING_DIR="/home/${USER}/python"

if [ -d "$WORKING_DIR" ]; then
  
  rm -rf "$WORKING_DIR"
  
fi
mkdir "$WORKING_DIR"
cd "$WORKING_DIR"

echo "================================================================================================================================"
echo "Downloading python ..."

wget https://www.python.org/ftp/python/3.7.9/Python-3.7.9.tar.xz

echo "================================================================================================================================"
echo "Extracting python ..."

tar -xf Python-3.7.9.tar.xz

echo "================================================================================================================================"
echo "Configure python ..."

cd Python-3.7.9
./configure --enable-optimizations


echo "================================================================================================================================"
echo "Make python ..."

make -j 8


echo "================================================================================================================================"
echo "Installing python ..."

sudo make alt install

echo "================================================================================================================================"
echo "Test python Version ..."

python3.7 --version


echo "================================================================================================================================"
echo "Installing python done."


echo "================================================================================================================================"
echo "Installing python pip ..."

sudo apt install python3-pip


echo "================================================================================================================================"
echo "Installing python pip done."


echo "================================================================================================================================"
echo "Installing pandas ..."

pip3 install pandas


echo "================================================================================================================================"
echo "Installing pandas done."


echo "================================================================================================================================"
echo "Installing loguru ..."

pip3 install loguru


echo "================================================================================================================================"
echo "Installing loguru done."


echo "================================================================================================================================"
echo "Installing pyspark ..."

pip3 install pyspark


echo "================================================================================================================================"
echo "Installing pyspark done."



 
